    private void HandleLoginAttempt() {
        remainingtries--;
        if (remainingtries > 0) {
            JOptionPane.showMessageDialog(this, "Password is incorrect. Please try again.");
            RemainingTriesLabel.setText("Remaining Login Attempts (" + remainingtries + "/" + maxtries + ")");
            return;
        }
        
        // Disable UI if max login attempts have been reached
        UsernameField.setEnabled(false);
        PasswordField.setEnabled(false);
        LoginButton.setEnabled(false);
        
        // Countdown timer for system lock
        secondsremaining = locktime;
        SystemLockLabel.setVisible(true);
        RemainingTriesLabel.setText("Maximum number of login attempts reached. ");
        SystemLockLabel.setText("System locked for " + secondsremaining +" seconds.");
        

        // Every 1 second, tick down and update the label
        countdowntimer = new Timer(1000, e -> {
            secondsremaining--;
            RemainingTriesLabel.setText("Maximum number of login attempts reached. ");
            SystemLockLabel.setText("System locked for " + secondsremaining +" seconds.");
            if (secondsremaining <= 0) {
                countdowntimer.stop();
            }
         });
        countdowntimer.start();
        
        // Timer for system lock
        unlocktimer = new Timer(locktime * 1000, e -> {
            UsernameField.setEnabled(true);
            PasswordField.setEnabled(true);
            LoginButton.setEnabled(true);
            SystemLockLabel.setVisible(false);
            RemainingTriesLabel.setVisible(false);
            remainingtries = maxtries;
        });
        unlocktimer.setRepeats(false);
        unlocktimer.start();
    }

