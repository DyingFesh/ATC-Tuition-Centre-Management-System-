    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        boolean found = false;
        
        try {
            File passwordfile = new File("Text Files/users.txt");
            FileReader fr = new FileReader(passwordfile);
            BufferedReader br = new BufferedReader(fr);

            String line;

            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                String id = parts[0];
                String username = parts[1];
                String password = parts[2];

                if (username.equalsIgnoreCase(UsernameField.getText())) {
                    found = true;
                    String userid = parts[0];
                    char role = userid.charAt(0);
                    
                    switch(role) {

                        // Admin login
                        case 'A':
                            RemainingTriesLabel.setVisible(false);
                            SystemLockLabel.setVisible(false);
                            if (password.equals(new String(PasswordField.getPassword()))) {
                                JOptionPane.showMessageDialog(null, "Login successful!");
                                AdminSession.setAdminid(parts[0]);

                                try {
                                    new AdminFrame().setVisible(true);
                                } catch (Exception ex) {
                                    ex.printStackTrace();
                                }
                                this.dispose();
                                
                            } else {
                                JOptionPane.showMessageDialog(null, "Password is incorrect. Please try again.");
                            }
                            
                            return;

                        // Student login
                        case 'S':
                            if (password.equals(new String(PasswordField.getPassword()))) {
                                JOptionPane.showMessageDialog(null, "Login successful!");
                                remainingtries = maxtries;
                                StudentSession.setStudentid(parts[0]);

                                try {
                                    new StudentPage().setVisible(true);
                                } catch (Exception ex) {
                                    ex.printStackTrace();
                                }
                                this.dispose();
                                
                            } else {
                                HandleLoginAttempt();
                                RemainingTriesLabel.setText("Remaining Login Attempts (" + remainingtries + "/" + maxtries + ")");
                                RemainingTriesLabel.setVisible(true);
                            }
                            
                            return;

                        // Tutor login
                        case 'T':                    
                            if (password.equals(new String(PasswordField.getPassword()))) {
                                JOptionPane.showMessageDialog(null, "Login successful!");
                                remainingtries = maxtries; 

                                try {
                                    new atctdgui(userid).setVisible(true);
                                } catch (Exception ex) {
                                    ex.printStackTrace();
                                }
                                this.dispose();
                                
                            } else {
                                HandleLoginAttempt();
                                RemainingTriesLabel.setText("Remaining Login Attempts (" + remainingtries + "/" + maxtries + ")");
                                RemainingTriesLabel.setVisible(true);
                            }
                            
                            return;

                        default:
                            JOptionPane.showMessageDialog(null, "Unknown role: " + role);
                            return;
                    }
                }
            }
            
            if (!found) {
                JOptionPane.showMessageDialog(this, "User not found. Please try again.");
            }
            
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

