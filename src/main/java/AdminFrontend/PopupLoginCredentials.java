    private void EditUsernameToggleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditUsernameToggleActionPerformed
        UsernameField.setEditable(true);
        if (EditUsernameToggle.isSelected()) {
            EditUsernameToggle.setText("SAVE");
            UsernameField.setBackground(new java.awt.Color(255, 255, 255));
            okbutton.setEnabled(false);
            EditPasswordToggle.setEnabled(false);
        } else {
            EditUsernameToggle.setText("EDIT");
            UsernameField.setBackground(new java.awt.Color(214, 217, 223));
            EditPasswordToggle.setEnabled(true);
            
            boolean changed = !UsernameField.getText().trim().equals(oldusername) || !PasswordField.getText().trim().equals(oldpassword);
            okbutton.setEnabled(changed);
        }
    }//GEN-LAST:event_EditUsernameToggleActionPerformed

