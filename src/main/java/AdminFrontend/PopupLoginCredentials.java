    public PopupLoginCredentials(List<String> logincreds) {
        initComponents();
        oldusername = logincreds.get(0);
        oldpassword = logincreds.get(1);
        UsernameField.setText(oldusername);
        PasswordField.setText(oldpassword);
        okbutton.setEnabled(false);
        
        okbutton.addActionListener(e -> {
            choice[0] = 0;
            Window w = SwingUtilities.getWindowAncestor(okbutton);
            if (w != null) w.dispose();
        });

        this.cancelbutton.addActionListener(e -> {
            choice[0] = 1;
            Window w = SwingUtilities.getWindowAncestor(cancelbutton);
            if (w != null) w.dispose();
        });
    }
    
