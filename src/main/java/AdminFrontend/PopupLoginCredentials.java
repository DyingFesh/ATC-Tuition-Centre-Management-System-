    private void UsernameFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_UsernameFieldFocusLost
        GeneralMethods.InputValidation(UsernameField, oldusername, 5, 20, "[a-zA-Z0-9]+", "Invalid username entered.\nPlease make sure uinput does not contain any illegal characters and is within 5-20 characters.");
        okbutton.setEnabled(false);
    }//GEN-LAST:event_UsernameFieldFocusLost

    private void PasswordFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_PasswordFieldFocusLost
        GeneralMethods.InputValidation(PasswordField, oldpassword, 8, Integer.MAX_VALUE, "^.+$", "Please enter a password that is at least 8 characters long.");
        okbutton.setEnabled(false);
    }//GEN-LAST:event_PasswordFieldFocusLost


