    private void DeleteStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteStudentButtonActionPerformed
        String selectedsid = saveselectedstudent.getStudentid();
        int choice = JOptionPane.showConfirmDialog(this, "Delete student " + saveselectedstudent.getStudentname() + " (" + selectedsid + ")?", "Deletion Confirmation", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            StudentMethods.DeleteStudent(selectedsid);
            JOptionPane.showMessageDialog(null, "Student successfully deleted.");
            parent.switchPanel("student");
        } else {
            return;
        }

    }//GEN-LAST:event_DeleteStudentButtonActionPerformed

