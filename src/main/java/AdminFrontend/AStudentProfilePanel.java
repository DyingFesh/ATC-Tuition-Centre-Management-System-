    private void ChangeStudentLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeStudentLoginButtonActionPerformed
        PopupLoginCredentials popup = new PopupLoginCredentials(saveselectedlogin);
        JButton[] options = {popup.getLCOKButton(), popup.getLCCancelButton()};
        JButton okbutton = options[0];
        
        while (true) {
            JOptionPane.showOptionDialog(this, popup, "Change Login Credentials", JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, options, okbutton);
            
            if (popup.getLCChoice() == 0) {
                if (GeneralMethods.ValidateUsername(saveselectedlogin.get(0).trim(), popup.getNewUsername().trim())) {
                    int choice2 = JOptionPane.showConfirmDialog(this, "Confirm changes to student login credentials?", "Login Credentials Change Confirmation", JOptionPane.YES_NO_OPTION);
                    if (choice2 == JOptionPane.OK_OPTION) {
                        String newusername = popup.getNewUsername();
                        String newpassword = popup.getNewPassword();
                        GeneralMethods.SaveNewLogin(saveselectedstudent.getStudentid(), newusername, newpassword);
                        CPStudentUsernameLabel.setText(newusername);
                        saveselectedlogin.set(0, newusername);
                        saveselectedlogin.set(1, newpassword);
                        popup.setUsername(newusername);
                        popup.setPassword(newpassword);
                        revalidate();
                        repaint();
                        break;
                    }
                } else {
                    popup.getUsernameField().setText(saveselectedlogin.get(0));
                }
            } else {
                break;
            }
        } 

        
            
    }//GEN-LAST:event_ChangeStudentLoginButtonActionPerformed

